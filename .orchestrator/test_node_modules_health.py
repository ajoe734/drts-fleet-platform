from __future__ import annotations

import importlib.util
import sys
import tempfile
import unittest
from pathlib import Path
from unittest.mock import patch


ROOT = Path(__file__).resolve().parent.parent
MODULE_PATH = ROOT / "scripts" / "ensure-local-node-modules.py"
SPEC = importlib.util.spec_from_file_location("ensure_local_node_modules", MODULE_PATH)
assert SPEC and SPEC.loader
MODULE = importlib.util.module_from_spec(SPEC)
sys.modules[SPEC.name] = MODULE
SPEC.loader.exec_module(MODULE)


def _write_modules_yaml(root: Path, virtual_store_dir: str) -> None:
    node_modules = root / "node_modules"
    node_modules.mkdir(parents=True, exist_ok=True)
    (node_modules / ".pnpm").mkdir(parents=True, exist_ok=True)
    (node_modules / ".modules.yaml").write_text(
        '{\n'
        f'  "virtualStoreDir": "{virtual_store_dir}"\n'
        '}\n',
        encoding="utf-8",
    )


class NodeModulesHealthTests(unittest.TestCase):
    def test_check_reports_missing_when_node_modules_absent(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            health = MODULE.check_node_modules_health(Path(tmp))

        self.assertFalse(health.healthy)
        self.assertEqual(health.status, "missing")

    def test_check_passes_when_virtual_store_is_local(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            _write_modules_yaml(root, ".pnpm")

            health = MODULE.check_node_modules_health(root)

        self.assertTrue(health.healthy)
        self.assertEqual(health.status, "healthy")

    def test_check_flags_external_virtual_store_dir(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            _write_modules_yaml(root, "../.artifacts/worktrees/auto/demo/node_modules/.pnpm")

            health = MODULE.check_node_modules_health(root)

        self.assertFalse(health.healthy)
        self.assertEqual(health.status, "polluted")
        self.assertIn("virtualStoreDir", health.details[0])

    def test_check_flags_external_pnpm_symlink(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            _write_modules_yaml(root, ".pnpm")
            external_store = root / ".artifacts/worktrees/auto/demo/node_modules/.pnpm/@types+react@19.2.14/node_modules/@types/react"
            external_store.mkdir(parents=True, exist_ok=True)
            scoped_dir = root / "node_modules/@types"
            scoped_dir.mkdir(parents=True, exist_ok=True)
            (scoped_dir / "react").symlink_to(external_store)

            health = MODULE.check_node_modules_health(root)

        self.assertFalse(health.healthy)
        self.assertEqual(health.status, "polluted")
        self.assertIn("symlinks escape", health.details[0])

    def test_repair_node_modules_invokes_ci_install(self) -> None:
        with tempfile.TemporaryDirectory() as tmp, patch.object(MODULE.subprocess, "run") as run:
            run.return_value.returncode = 0

            exit_code = MODULE.repair_node_modules(Path(tmp))

        self.assertEqual(exit_code, 0)
        _, kwargs = run.call_args
        self.assertEqual(kwargs["cwd"], Path(tmp))
        self.assertEqual(kwargs["env"]["CI"], "true")


if __name__ == "__main__":
    unittest.main()
